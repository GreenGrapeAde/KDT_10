## if ~else 조건문과 조건부표현식
## --------------------------------------------------------
## 양수, 음수, 0 출력하기
num = 9

if num > 0:
    print("양수")
elif num < 0:
    print("음수")
else:
    print("영")

## ==> 조건부 표현식
result = "양수" if num > 0 else "음수" if num < 0 else '영'
#               --(1)--                --(2)--               조건문부터 읽음
