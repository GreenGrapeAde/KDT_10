## --------------------------------------------------------
## 입력 데이터 종류를 출력하기---------------------------------
## -> '2342'  :  숫자
## -> '가나다' :  글자
## -> 'ㅁ34'  :  숫자와 글자
jumsu = input("Please enter a message: ").strip()

if jumsu.isdecimal():
    print("숫자입니다.")

elif jumsu.isalpha():
    print("글자입니다.")

elif jumsu.isalnum():
    print("숫자와 글자입니다.")

else:
    print("키보드 기호들입니다.")
