## --------------------------------------------------------
## for ~ in 반복문 : 중첩 반복문
## --------------------------------------------------------
## 9단만 출력

print("9단만 출력")
for i in range(1, 10):
    print(f"9 * {i} = {9 * i:>2}")
print()

print("전체 구구단")
for i in range(2, 10):
    for j in range(1, 10):
        print(f"{i} * {j} = {j * i:>2}")

print()

## 홀수단만 출력 3, 5, 7, 9 단
print("홀수단만 출력")
for i in range(3, 10, 2):
    for j in range(1, 10):
        print(f"{i} * {j} = {j * i:>2}")